def fetch_remote_image(img_url: str) -> str:
    uri: Path = Path(img_url)
    fpath: str = f'/tmp/{uri.stem}{uri.suffix}'
    with open(fpath, 'wb') as handle:
        response = requests.get(img_url, stream=True)
        if not response.ok:
            raise Exception(f'Failed to fetch img {img_url}, got response {response}')
        for block in response.iter_content(1024):
            if not block:
                break
            handle.write(block)
    return fpath


